function convertirAMayusculas() {
    const areaTexto = document.getElementById('areaTexto');
    areaTexto.value = areaTexto.value.toUpperCase();
  }
  
  function convertirAMinusculas() {
    const areaTexto = document.getElementById('areaTexto');
    areaTexto.value = areaTexto.value.toLowerCase();
  }
  